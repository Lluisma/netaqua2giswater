
CREATE OR REPLACE VIEW CAT_FEATURE AS 

	SELECT 	'TRAM'			  id,
			    'CONDUIT'	    system_id,
			    'ARC'			    feature_type
	FROM DUAL
  UNION
	SELECT	'RIERA',        'CONDUIT',	'ARC'    	FROM DUAL
  UNION
	SELECT	'CUNETA',       'CONDUIT',	'ARC'     FROM DUAL
	UNION
	SELECT	'CLAVEGUERO',   'CONNEC',	  'CONNEC'	FROM DUAL
	UNION
	SELECT	'REIXA',		    'GULLY',	  'GULLY'  	FROM DUAL
	UNION
	SELECT	'EMBORNAL',		  'GULLY',	  'GULLY'  	FROM DUAL
	UNION
	SELECT  'CAMBRA',		    'CHAMBER',  'NODE'		FROM DUAL
	UNION
	SELECT  'BOMBAMENT',		'CHAMBER',  'NODE'		FROM DUAL
	UNION
	SELECT  'SOBREEIXIDOR',	'CHAMBER', 	'NODE'		FROM DUAL
	UNION	
	SELECT	'UNIO',	        'JUNCTION', 'NODE'		FROM DUAL
	UNION
  SELECT	'NFICTICI',	    'JUNCTION',	'NODE'		FROM DUAL
	UNION
	SELECT	'REGISTRE',			'MANHOLE',  'NODE'		FROM DUAL
	UNION
	SELECT	'SORRER',       'NETINIT',  'NODE'		FROM DUAL
	UNION
	SELECT	'INICI',        'NETINIT',  'NODE'		FROM DUAL
	UNION
	SELECT	'SUMIDERO',     'NETINIT',  'NODE'		FROM DUAL
	UNION
	SELECT	'DESGUAS',			'OUTFALL',  'NODE'		FROM DUAL
	UNION
  SELECT	'DESCARREGA',		'OUTFALL',  'NODE'		FROM DUAL
	UNION
	SELECT	'VALVULA',		  'VALVE',    'NODE'		FROM DUAL
	UNION
	SELECT	'VENTOSA',		  'VALVE',    'NODE'		FROM DUAL
	UNION
	SELECT	'VRETENCIO',		'VALVE',    'NODE'		FROM DUAL
	ORDER BY 3, 2, 1;
